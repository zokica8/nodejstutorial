'use strict';

// addition
console.log(10 + 32);

// subtraction
console.log(10 - 32);

// multiplier
console.log(10 * 8);

// division
console.log(10 / 8); // here, you will get a decimal number, not a whole number (like in c++, java and other languages)

// the remainder from the division - modulus (remainder)
console.log(10 % 8);
console.log(16.6 % 6); // can work with decimal numbers in NodeJS

// increment, decrement (prefix and postfix), same like in Java
let i = 6;
let j = 0;
console.log(++i);
console.log(--j);
console.log(i++, i);
console.log(j--, j);

// exponentiation (**) raises the first operand to the power of the second operand
let k = 10;
k = k ** 2;
console.log(k);
